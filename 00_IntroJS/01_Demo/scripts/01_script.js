// Else-If_kette (verschachtelt)

let punktzahl = 35;

if (punktzahl >= 90) {
    console.log("Sehr gut. Erreichte Punktzahl:"+ punktzahl);
} else if (punktzahl >= 75) {
    console.log("Gut. Erreichte Punktzahl:"+ punktzahl);
} else if (punktzahl >= 50) {
    console.log("Genügend. Erreichte Punktzahl:"+ punktzahl);
} else {
    console.log("Ungenügend. Erreichte Punktzahl:"+ punktzahl);
}