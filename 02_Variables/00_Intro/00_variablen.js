/*
Thema: Variablen und Konstanten

Wichtig, wenn Werte einer Variable zugewiesen werden:
Das '='-Zeichen ist ein Zuweisung-Operator und
nicht ein mathematisches Gleichheitszeichen!!!
 */

// Variable deklarieren
let zahl1;
let zahl2;

// Variable initialisieren
zahl1 = 30;
zahl2 = 15;

// Konstante deklarieren und initalisieren
const maxBetrag = 100;

// Variablen subtrahieren und Wert ausgeben (gibt den Wert 15 aus)
console.log(zahl1 - zahl2);

console.log(maxBetrag - zahl1 - zahl2);